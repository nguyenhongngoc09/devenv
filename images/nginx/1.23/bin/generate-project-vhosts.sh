#!/bin/sh
set -eu

projects_root=/var/www/html/projects
log_root=/var/log/nginx
generated=/etc/nginx/conf.d/generated/projects.generated.conf

mkdir -p "$(dirname "$generated")"
: > "$generated"

{
  echo '# generated by docker-entrypoint; do not edit'
  echo

  if [ -d "$projects_root" ]; then
    find "$projects_root" -mindepth 1 -maxdepth 1 -type d | sort | while IFS= read -r project_dir; do
      project_name=$(basename "$project_dir")

      case "$project_name" in
        ''|.*|*[^A-Za-z0-9_-]*)
          continue
          ;;
      esac

      public_dir="$projects_root/$project_name/public"
      log_dir="$log_root/$project_name"

      mkdir -p "$log_dir"

      cat <<EOF
location = /$project_name {
  return 301 /$project_name/;
}

location /$project_name/ {
  alias $public_dir/;
  access_log $log_dir/access.log main;
  error_log $log_dir/error.log;
  index index.php index.html index.htm;
  error_page 404 = @${project_name}_front_controller;
}

location ~ ^/$project_name(?<script>/.*\.php)$ {
  access_log $log_dir/access.log main;
  error_log $log_dir/error.log;
  include fastcgi_params;
  fastcgi_pass fastcgi_backend;
  fastcgi_index index.php;
  fastcgi_param SCRIPT_FILENAME ${public_dir}\$script;
  fastcgi_read_timeout 600;
}

location @${project_name}_front_controller {
  internal;
  access_log $log_dir/access.log main;
  error_log $log_dir/error.log;
  include fastcgi_params;
  fastcgi_pass fastcgi_backend;
  fastcgi_index index.php;
  fastcgi_param SCRIPT_FILENAME $public_dir/index.php;
  fastcgi_read_timeout 600;
}

EOF
    done
  fi
} > "$generated"
